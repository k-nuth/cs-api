namespace Bitprim
{
    /// <summary>
    /// Represents the supported coins
    /// </summary>
    public enum CurrencyType
    {
        /// <summary>
        /// Default value
        /// </summary>
        None = 0,
        /// <summary>
        /// Bitcoin (BTC)
        /// </summary>
        Bitcoin = 1,
        /// <summary>
        /// Bitcoint Cash (BCH)
        /// </summary>
        BitcoinCash = 2,
        /// <summary>
        /// Litecoin (LTC)
        /// </summary>
        Litecoin = 3
    }
}